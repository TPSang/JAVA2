    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       StringBuilder sb = new StringBuilder();
       if(txtUser.getText().equals("")){
            sb.append("Username không được empty\n");
       }
       if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
           return;
       }
       QLuser user = new QLuser();
       user.setUsername(txtUser.getText());
       user.setPassword(new String(txtPass.getPassword()));
       user.setRole(rdoUser.isSelected()? "User" : "Admin");
       list.add(user);
       insertTable();
       JOptionPane.showMessageDialog(this, "User đã được insert");
        btnResetActionPerformed(evt);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        StringBuilder sb = new StringBuilder();
       if(txtUser.getText().equals("")){
            sb.append("Username không được empty\n");
       }
       if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
           return;
       }
        for (QLuser user : list) {
            if(user.getUsername().equals(txtUser.getText())){
                int chon = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa?","Thông báo",JOptionPane.YES_NO_OPTION);
                if(chon == JOptionPane.YES_OPTION){
                    list.remove(user);
                    insertTable();
                    JOptionPane.showMessageDialog(this,"User đã được xóa");
                    btnResetActionPerformed(evt);
                }
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Không tìm thấy User");
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       StringBuilder sb = new StringBuilder();
       if(txtUser.getText().equals("")){
            sb.append("Username không được empty\n");
       }
       if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
           return;
       }
        QLuser user = new QLuser();
       user.setUsername(txtUser.getText());
       user.setPassword(new String(txtPass.getPassword()));
       user.setRole(rdoUser.isSelected()? "User" : "Admin");
       int index = tblUsers.getSelectedRow();
           if(index == -1 ){
               JOptionPane.showMessageDialog(this,"chưa chọn dòng");
           } else{
               list.set(index, user);
               insertTable();
               tblUsers.setRowSelectionInterval(index, index);
               JOptionPane.showMessageDialog(this, "user update thành công");
           }    
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

