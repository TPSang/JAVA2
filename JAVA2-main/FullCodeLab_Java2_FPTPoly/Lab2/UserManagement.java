    private void insertTable(){
        while(tblModel.getRowCount()>0)
            tblModel.removeRow(0);
        for (QLuser user : list) {
            tblModel.addRow(new String[]{user.getUsername(),user.getPassword(),user
            .getRole()});
        }
        tblModel.fireTableDataChanged();
    }
